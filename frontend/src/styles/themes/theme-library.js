// Theme comparison helper
export function compareThemes(theme1, theme2, isLight) {
  const themes = isLight ? lightThemes : darkThemes;
  const t1 = themes[theme1];
  const t2 = themes[theme2];
  
  if (!t1 || !t2) return null;
  
  return {
    primary: {
      theme1: t1.colors.primary,
      theme2: t2.colors.primary,
      difference: 'Compare primary colors'
    },
    background: {
      theme1: t1.colors.background,
      theme2: t2.colors.background,
      difference: 'Compare backgrounds'
    },
    personality: {
      theme1: t1.description,
      theme2: t2.description
    }
  };
}
